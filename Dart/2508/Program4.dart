// switch

void main() {
  double x = 0.5;
  switch (x) {
    case 0.5:
      print("x = $x");
    case 0.5:
      print("x = $x");
    case 1.0:
      print("x = $x");
    default:
      print("No match");
  }
}


// duplicate cases are allowed in switch statement in dart
// dart also accepts double values in switch case